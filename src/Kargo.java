public class Kargo {
    public int takipNo;
    public String gonderici;
    public String alici;
    public String kargoDurum;

    public Kargo(int takipNo, String gonderici, String alici, String kargoDurum) {
        this.takipNo = takipNo;
        this.gonderici = gonderici;
        this.alici = alici;
        this.kargoDurum = kargoDurum;
    }
}
